import java.util.*;
public class Triangle1 {
    public static void main(String[] args) {
        Scanner x = new Scanner (System.in);
        
        int top = 5;
        
        for(int i=0; i<=top; i++){
        
            for(int k=i; k<top; k++){
                System.out.print("*");
            }
            
            System.out.println("");
        }
        
    }  
}
